    public function findOrFail(int $id): Product
    {
        $product = $this->find($id);

        if(!$product) 
        {
            $exceptionData = new ServiceExceptionData(404,'Product Not Found');  
            throw new ServiceException($exceptionData);
        }
        
        return $product;
